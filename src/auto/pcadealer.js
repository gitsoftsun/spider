var Dealer = function(){
    this.resultDir = "../../result/";
    this.dataDir = '../../appdata/';
    this.resultFile = "pcadealer.txt";
    this.progressFile = "pcadealer_progress.txt";
    this.done = {};
    this.curPageIdx = 1;
    this.tasks = [];
}

Dealer.prototype.init = function(){
    if(fs.existsSync(this.resultDir+this.progressFile)){
	fs.readFileSync(this.resultDir+this.progressFile).toString().split('\n').reduce(function(pre,cur){
	    if(cur){
		pre[cur]=true;
	    }
	    return pre;
	},this.done);
    }
    for(var i=0;i<brands.length;i++){
	var brand = brands[i];
	this.tasks.push({path:brand.path,name:brand.name,page:1});
    }
    console.log("[INFO] task count: %d",this.tasks.length);
}

Dealer.prototype.start = function(){
    this.init();
    this.wgetList();
}

Dealer.prototype.wgetList = function(t){
    if(!t){
	var t = null;
	do{
	    t = this.tasks.shift();
	}
	while(this.tasks.length && this.done[t.name])
	if(!t){
	    console.log("[DONE] job done");
	    return;
	}
    }
    
    var host = "price.pcauto.com.cn";
    var path = t.path + "p"+t.page+".html";
    
    var opt = null;
    opt = new helper.basic_options(host,path);
    
    opt.agent = false;
    console.log("[GET ] %s,%d/%d",t.name,t.page,t.maxPage);
    helper.request_data(opt,null,function(data,args,res){
	that.processList(data,args,res);
    },t);
}

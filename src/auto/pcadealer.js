Dealer.prototype.processList = function(data,args,res){
    if(!data){
	console.log("[ERROR] data empty");
	setTimeout(function(){
	    that.wgetList(args[0]);
	},3000);
	return;
    }
    var $ = cheerio.load(data);
    if(args[0].maxPage==undefined){
	var nextPage = $(".pcauto_page a.next").last();
	if(nextPage){
	    args[0].maxPage = Number(nextPage.prev().text());
	}else{
	    args[0].maxPage = 1;
	}
    }
    
    var records = [""];
    $("ul.yStore > li").each(function(){
	var titleLink = $("div.divYSa dl dd i",this).first();
	var type = $("span.dsTitle",titleLink).text();
	type = type && type.replace(/[：:]/g,"");
	
	var name = $("strong",titleLink).text().trim();
	name = name && name.replace(/[,，]/g,"");
	
	var audit = $("img",titleLink).length;
	var phone = $("div.divYSa dl dd i.tel strong.red",this).text();
	if(!phone){
	    phone = $("div.divYSa dl dd i.tel",this).text();
	}
	phone = phone && phone.replace(/[\s电话:：]/g,"");
	var city = $("div.divYSb i",this).eq(1).text();
	var o = [args[0].name,name,city,phone,type,audit?"Y":"N"].join('\t');
	records.push(o);
    });
    fs.appendFileSync(this.resultDir+this.resultFile,records.join('\n'));
    
    if(args[0].page<args[0].maxPage){
	args[0].page++;
	setTimeout(function(){
	    that.wgetList(args[0]);
	},0);
    }else{
	console.log("[DONE] %s",args[0].name);
	fs.appendFileSync(this.resultDir+this.progressFile,args[0].name+"\n");
	this.wgetList();
    }
}

